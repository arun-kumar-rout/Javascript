// array = group of values

var array = [1,2,3,4,5,6,7,8];

// push pop shift unshift
array.push(9);  // added 9 in the last
array.pop(9);   // removed 9 added by the previous code
array.unshift(); // add a new data in the starting of the array
array.shift() // remove a existing data from the starting of the array
array.slice(1,2) // remove 2 data from the index 1 of the array